package com.netty.model.player.skill.crafting;

public class Crafting {

	private Cape cape;
	private Gem gem;
	private Glass glass;
	private GoldJewellery goldJewellery;
	private Leather leather;
	private Pottery pottery;
	private Silver silver;
	private SpinningWool spinningWool;
	private Staff staff;
	private Weaving weaving;

	public void setCape(Cape cape) {
		this.cape = cape;
	}

	public Cape getCape() {
		return this.cape;
	}

	public void setGem(Gem gem) {
		this.gem = gem;
	}

	public Gem getGem() {
		return this.gem;
	}

	public void setGlass(Glass glass) {
		this.glass = glass;
	}

	public Glass getGlass() {
		return this.glass;
	}

	public void setGoldJewellery(GoldJewellery goldJewellery) {
		this.goldJewellery = goldJewellery;
	}

	public GoldJewellery getGoldJewellery() {
		return this.goldJewellery;
	}

	public void setLeather(Leather leather) {
		this.leather = leather;
	}

	public Leather getLeather() {
		return this.leather;
	}

	public void setPottery(Pottery pottery) {
		this.pottery = pottery;
	}

	public Pottery getPottery() {
		return this.pottery;
	}

	public void setSilver(Silver silver) {
		this.silver = silver;
	}

	public Silver getSilver() {
		return this.silver;
	}

	public void setSpinningWool(SpinningWool spinningWool) {
		this.spinningWool = spinningWool;
	}

	public SpinningWool getSpinningWool() {
		return this.spinningWool;
	}

	public void setStaff(Staff staff) {
		this.staff = staff;
	}

	public Staff getStaff() {
		return this.staff;
	}

	public void setWeaving(Weaving weaving) {
		this.weaving = weaving;
	}

	public Weaving getWeaving() {
		return this.weaving;
	}
}
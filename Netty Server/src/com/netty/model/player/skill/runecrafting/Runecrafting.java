package com.netty.model.player.skill.runecrafting;

public class Runecrafting {

	private Altar altar;
	private Pouch pouch;
	private Rune rune;
	private Staff staff;
	private Talisman talisman;
	private Tiara tiara;

	public void setAltar(Altar altar) {
		this.altar = altar;
	}

	public Altar getAltar() {
		return this.altar;
	}

	public void setPouch(Pouch pouch) {
		this.pouch = pouch;
	}

	public Pouch getPouch() {
		return this.pouch;
	}

	public void setRune(Rune rune) {
		this.rune = rune;
	}

	public Rune getRune() {
		return this.rune;
	}

	public void setStaff(Staff staff) {
		this.staff = staff;
	}

	public Staff getStaff() {
		return this.staff;
	}

	public void setTalisman(Talisman talisman) {
		this.talisman = talisman;
	}

	public Talisman getTalisman() {
		return this.talisman;
	}

	public void setTiara(Tiara tiara) {
		this.tiara = tiara;
	}

	public Tiara getTiara() {
		return this.tiara;
	}
}